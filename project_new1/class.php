        <h2>Materi: Class Diagram</h2>
        <p>
        Class Diagram adalah diagram yang menggambarkan struktur statis dari sistem, termasuk kelas-kelas, atribut, 
        metode, dan hubungan antara kelas-kelas tersebut. Ini adalah salah satu diagram UML paling umum dan penting 
        untuk merancang arsitektur perangkat lunak berbasis objek.
        </p>
        <h4>Contoh Class Diagram:</h4>
        <img src="class.png" alt="Class Diagram" style="max-width: 100%; height: auto;">
        <h3>Elemen Penting:</h3>
        <ul>
            <li><strong>Class:</strong>Mewakili entitas dalam sistem, ditampilkan sebagai persegi panjang yang dibagi 
            menjadi tiga bagian (nama kelas, atribut, dan metode).</li>
            <li><strong>Attributes:</strong>Properti atau data yang dimiliki oleh kelas.</li>
            <li><strong>Methods:</strong>Fungsi atau operasi yang bisa dilakukan oleh kelas.</li>
            <li><strong>Relationships:</strong>Hubungan antara kelas, seperti association, inheritance, aggregation, dan composition.</li>
        </ul>
        <h3>Fungsi Utama:</h3>
        <ul>
            <li>Menggambarkan struktur kelas dan hubungan antar kelas dalam sistem.</li>
            <li>Membantu dalam merancang dan memahami struktur objek pada program.</li>      
        </ul>
